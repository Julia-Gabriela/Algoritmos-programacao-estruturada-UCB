#include <stdio.h>
#include <math.h>
//incrementando e decrementando valores
int main()
{
int valor = 10;

    valor ++;
    printf("Valor depois do incremento: %d\n", valor);
    valor --;
    printf("Valor depois do incremento: %d\n", valor);
    valor += 2;
    printf("Valor depois do incremento: %d\n", valor);
    valor -= 3;
    printf("Valor depois do incremento: %d\n", valor);
    valor *= 3;
    printf("Valor depois do incremento: %d\n", valor);
    valor /= 3;
    printf("Valor depois do incremento: %d\n", valor);
}